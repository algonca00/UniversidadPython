# La funcion input() nos regresa un string, osea que si queremos realizar operaciones, cambiamos el tipo con la funcion int
num1 = int(input("Ingresa un valor numerico: "))
print("num1 es igual a:",num1)
num2 = int(input("Ingresa otro valor numerico: "))
print("num2 es igual a:",num2)
print("Y el resultado de num1 + num2 es:",num1+num2)
